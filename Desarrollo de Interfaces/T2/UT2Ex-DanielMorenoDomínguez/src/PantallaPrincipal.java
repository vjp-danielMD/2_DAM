    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int fila = this.jTable1.getSelectedRow();
        if (fila >= 0 && fila < this.logica.getListArticulos().size()) {
            logica.eliminarPelicula(fila);
            jTable1.setModel(new MyTableModel(logica.getListArticulos()));
            JOptionPane.showMessageDialog(this, "Articulo eliminado.", "Atención", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "No hay fila seleccionada", "Atención", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

