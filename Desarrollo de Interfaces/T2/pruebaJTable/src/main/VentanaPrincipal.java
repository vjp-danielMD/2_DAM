    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // conseguir fila seleccionada
        int fila = this.jTable1.getSelectedRow();

        if (fila >= 0 && fila < this.logicaNegocio.getListaAlumnos().size()) {
            // borrar el objeto del arraylist en esa posicion
            logicaNegocio.getListaAlumnos().remove(fila);
            JOptionPane.showMessageDialog(this, "Usuario eliminado.", "Atencion", JOptionPane.INFORMATION_MESSAGE);
            // actualizar tabla
            this.jTable1.updateUI();
        } else {
            JOptionPane.showMessageDialog(this, "No hay fila seleccionada", "Atencion", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_jButton3ActionPerformed

