    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser chooser = new JFileChooser();
        int resultado = chooser.showOpenDialog(null);
        if (resultado == JFileChooser.APPROVE_OPTION) {
            File archivo = chooser.getSelectedFile();
            try (BufferedReader br = new BufferedReader(new FileReader(archivo))) {
                jTextArea1.setText("");
                String linea;
                while ((linea = br.readLine()) != null) {
                    jTextArea1.append(linea + "\n");
                }
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Error al abrir archivo");
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        JFileChooser chooser = new JFileChooser();
        int resultado = chooser.showSaveDialog(null);
        if (resultado == JFileChooser.APPROVE_OPTION) {
            File archivo = chooser.getSelectedFile();
            try (BufferedWriter bw = new BufferedWriter(new FileWriter(archivo))) {
                bw.write(jTextArea1.getText());
                JOptionPane.showMessageDialog(null, "Documento guardado correctamente");
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Error al guardar archivo");
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

