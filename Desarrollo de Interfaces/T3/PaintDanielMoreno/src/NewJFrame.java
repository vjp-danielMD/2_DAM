    private void pintar(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pintar
        int x = evt.getX();
        int y = evt.getY();
        graficos.setColor(color);
        graficos.fillOval(x, y, jSlider1.getValue(), jSlider1.getValue());
        jLabel1.updateUI();

    }//GEN-LAST:event_pintar

