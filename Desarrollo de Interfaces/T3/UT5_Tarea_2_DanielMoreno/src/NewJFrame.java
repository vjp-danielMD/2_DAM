    private void dibujarFigura(java.awt.event.MouseEvent evt) {
        if (primerClick) {
            x1 = evt.getX();
            y1 = evt.getY();
            primerClick = false;
        } else {
            int x2 = evt.getX();
            int y2 = evt.getY();
            primerClick = true;

            g2d.setColor(color);
            g2d.setStroke(new BasicStroke(3));

            int ancho = Math.abs(x2 - x1);
            int alto = Math.abs(y2 - y1);
            int x = Math.min(x1, x2);
            int y = Math.min(y1, y2);

            switch (modo) {
                case "cuadrado":
                    g2d.drawRect(x, y, ancho, alto);
                    break;
                case "circulo":
                    int diametro = Math.max(ancho, alto);
                    g2d.drawOval(x, y, diametro, diametro);
                    break;
                case "hexagono":
                    int r = Math.max(ancho, alto) / 2;
                    int cx = (x1 + x2) / 2;
                    int cy = (y1 + y2) / 2;
                    int[] xs = new int[6];
                    int[] ys = new int[6];
                    for (int i = 0; i < 6; i++) {
                        xs[i] = cx + (int) (r * Math.cos(i * Math.PI / 3));
                        ys[i] = cy + (int) (r * Math.sin(i * Math.PI / 3));
                    }
                    g2d.drawPolygon(xs, ys, 6);
                    break;
                case "linea":
                    g2d.drawLine(x1, y1, x2, y2);
                    break;
            }

            jLabel1.updateUI();
        }
    }


