package com.example.victormerida_ejercicio13.ui.linearlayout

import android.os.Bundle
import android.view.LayoutInflater
import android.view.View
import android.view.ViewGroup
import android.widget.TextView
import androidx.fragment.app.Fragment
import androidx.lifecycle.ViewModelProvider
import com.example.victormerida_ejercicio13.databinding.FragmentLinearlayoutBinding

class linearFragment : Fragment() {

    private var _binding: FragmentLinearlayoutBinding? = null

    // This property is only valid between onCreateView and
    // onDestroyView.
    private val binding get() = _binding!!

    override fun onCreateView(
        inflater: LayoutInflater,
        container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View {
        val linearViewModel =
            ViewModelProvider(this).get(LinearViewModel::class.java)

        _binding = FragmentLinearlayoutBinding.inflate(inflater, container, false)
        val root: View = binding.root


        return root
    }

    override fun onDestroyView() {
        super.onDestroyView()
        _binding = null
    }
}