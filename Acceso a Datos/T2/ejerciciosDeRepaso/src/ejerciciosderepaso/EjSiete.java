public class EjSiete {
    public static void main(String[] args) {
        mostrarNums();
    }
    
    public static void mostrarNums(){
        int num = 0;
        int contador = 0;
        
        while (contador < 100) {            
            num ++;
            contador ++;
            
            System.out.println(num);
        }
    }
}
